package com.amolik.data;

public class FiscalCorrectionField {

	private String imageName;
	private String fieldName;
	private String correctFieldValue;
	
	public String getImageName() {
		return imageName;
	}
	public void setImageName(String imageName) {
		this.imageName = imageName;
	}
	public String getFieldName() {
		return fieldName;
	}
	public void setFieldName(String fieldName) {
		this.fieldName = fieldName;
	}
	public String getCorrectFieldValue() {
		return correctFieldValue;
	}
	public void setCorrectFieldValue(String correctFieldValue) {
		this.correctFieldValue = correctFieldValue;
	}
	
	
}
